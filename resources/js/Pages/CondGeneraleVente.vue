<h3>Article 1 – Processus d’inscription</h3>
<div class="paragraph">
La réservation d’un séjour se déroulera suivant le processus décrit ci-après :<br>
<b>Etape 1 : </b> Emission de la demande d’inscription du client par mail ou par téléphone. <br>
<b>Etape 2 : </b> Envoi d’un bulletin d’inscription par L[P]AV. <br>
<b>Etape 3 : </b> Envoi du bulletin d’inscription complété à L[P]AV. <br>
<b>Etape 4 : </b>Validation de la disponibilité des lieux d’hébergement aux dates demandées par L[P]AV. <br>
<b>Etape 5 : </b> Envoi du bon de commande incluant les coordonnées et les caractéristiques des lieux d’hébergement par L[P]AV. <br>
<b>Etape 6 : </b> Validation du bon de commande par le client. <br>
<b>Etape 7 : </b> Paiement d’un acompte ou de la totalité du coût du séjour (voir article 2). <br>
</div>

<h3>Article 2 – Modalités de Règlement</h3>

<div class="paragraph">
Pour toute inscription réalisée à plus de 30 jours du départ, il sera procédé à un encaissement par L[P]AV d’un acompte de 30% du montant total du séjour. <br>
Pour toute inscription à moins de 30 jours de la date du départ, le règlement doit être effectué en une seule fois et pour la totalité du montant du séjour.<br>
Le paiement devra être effectué par virement bancaire sur le compte indiqué sur le bon de commande. <br>
A défaut d’encaissement par L[P]AV de toute ou partie du montant total du voyage dans les délais requis, nous ne pourrons pas garantir la demande d’inscription aux dates souhaitées et au prix annoncé. <br>
Tout retard dans le paiement d’un acompte ou du solde sera considéré comme une annulation de la part du client. Celui-ci restera toutefois redevable des frais qui auront été engagés à la date effective de l’annulation.<br>
Conformément à l’article L 221-28 du code de la consommation, le client ne bénéficie d’aucun délai de rétractation dés lors que la commande de prestations de voyage est validée et que les frais y afférent sont engagés.<br>
</div>

<h3>Article 3 – Assurance</h3>
<div class="paragraph">
Aucune assurance n’est prévue dans le cadre de la prestation.
Le client devra donc, s’il le souhaite, se rapprocher de sa propre assurance pour déterminer la nature des risques pour lesquels il souhaite être couvert.
</div>

<h3>Article 4 – Modifications demandées par le client</h3>
<div class="paragraph">
Le client a la possibilité de demander les modifications suivantes après la signature de la commande :
<ul>
<li> augmentation du nombre de participants</li>
<li> d’une nuit ou d’une option supplémentaire</li>
</ul>
L[P]AV mettra tout en œuvre pour satisfaire ces nouvelles demandes et transmettra un bon de commande complémentaire au client.
Après validation du bon de commande par le client, la prestation supplémentaire sera mise en œuvre et le client devra s’acquitter du supplément de prix correspondant.
</div>
<h3>Article 5 – Arrêt en cours de séjour</h3>
<div class="paragraph">
Le client qui souhaiterait interrompre son circuit à vélo devra contacter L[P]AV qui organisera le transfert des personnes des bagages et des vélos par taxi.
Les coûts supplémentaires engendrés seront à la charge du client lequel devra s’en acquitter immédiatement. Les prestations engagées et non consommées ne seront pas remboursées.
</div>
<h3>Article 6 – Cession du contrat</h3>
<div class="paragraph">
Conformément à l’article R.211-7 du Code du Tourisme, le client peut céder son contrat à un cessionnaire qui remplit les mêmes conditions que lui pour effectuer le circuit, tant que ce contrat n’a produit aucun effet.  Le client est tenu d’informer L[P]AV de sa décision par tout moyen permettant d’en obtenir un accusé de réception au plus tard sept jours avant le début du voyage.
En cas de cession de contrat, le cédant et le cessionnaire sont préalablement tenus d’acquitter les frais induits par cette cession.
</div>
 
<h3>Article 7 – Informations officielles</h3>
<div class="paragraph">
Avant de vous inscrire pour entreprendre votre voyage, vous devez vérifier que chacun des voyageurs, en fonction de sa situation personnelle et de sa nationalité, est en possession du passeport ou de la carte nationale d’identité (CNI) en cours de validité et qui sera utilisé(e) pour réaliser le circuit envisagé ainsi que tout autre(s) document(s) (visa, autorisation de sortie de territoire etc..) requis et conformes aux exigences pour transiter et/ou entrer dans le(s) pays du voyage.
<br>
Il appartient au client de vérifier la validité des documents en sa possession, et de s’assurer d’être en conformité avec les formalités douanières administratives et sanitaires du pays de destination et le cas échéant, des pays de transit. Il relève de la responsabilité de chaque voyageur de prendre connaissance des consignes de sécurité diffusées par les autorités locales et des recommandations aux voyageurs faites par le ministère de l’Europe et des Affaires étrangères (MEAE) sur le site www.diplomatie.gouv.fr et les respecter. Ces informations pouvant évoluer jusqu’à la date du départ, il est vivement conseillé de les consulter régulièrement.
<br>
Depuis le 15 janvier 2017, l’autorisation de sortie du territoire est nécessaire pour tout mineur qui voyage hors de France sans être accompagné de l’un de ses parents (titulaire de l’autorité parentale).
</div>
<h3>Article 8 – Transport de Bagages</h3>
<div class="paragraph">
Le transport des bagages d’un hébergement à l’autre est assuré par les compagnies de taxis.
Les bagages doivent être impérativement fermés, identifiés et déposés à la réception de l’hébergement.
Nous vous recommandons de conserver vos objets de valeur avec vous et de ne pas les laisser dans vos bagages lors du transport.
L[P]AV ne saurait être tenu pour responsable en cas de vol ou de perte lors du transport des bagages.
</div>
<h3>Article 9 – Location de vélos</h3>
<div class="paragraph">
<h4>8.1 : Retrait – retour du / des vélo(s)</h4>
Le retrait et le retour du / des vélo(s) et des équipements sont réalisés sur un lieu et aux horaires convenus avec le client.
Le lieu de retour du / des vélo(s) et des équipements, convenu entre les parties, est impératif.
 
<h4>8.2 : Dégradation – non-restitution</h4>
Le(s) vélo(s) et les équipements qui sont mis à la disposition du client sont présumés être en parfait état de fonctionnement.
Conformément à l’article 1242 du Code civil, le client est responsable du / des vélo(s) et des équipements qu’il a sous sa garde.
Le client est ainsi responsable en cas de vol, dégradation ou casse totale ou partielle du/des vélo(s).
En cas de non-restitution du / des vélo(s) à la date convenue quelle qu’en soit la cause, L[P]AV facturera au client le prix du vélo mentionné dans le bon de commande.
En cas de dégradation du / des vélo(s), le client supportera les montants correspondant aux dommages subis par le(s) vélo(s) pendant la location et résultant d’un usage anormal ou fautif du client.
En cas de dégradation importante (élément de sécurité du vélo manquant, cadre abîmé, tordu ou enfoncé, destruction de tout ou partie d’un composant ne figurant pas sur la liste des pièces d’usage), L[P]AV facturera au client le prix du vélo mentionné dans le bon de commande.
<h4>8.3 : Utilisation du / des vélo(s) et des équipements</h4>
Lors du l’utilisation du / des vélo(s) et des équipements, le client s’engage à :
<ul>
<li>Respecter la règlementation en vigueur et spécialement, le Code de la route en vigueur au moment de leur utilisation (ex. : utiliser les voies ouvertes à la circulation publique et non interdites à la circulation des cyclistes, respecter les feux, ne pas rouler sur les trottoirs, ne pas doubler un véhicule par la droite, etc.) ; </li>
<li>Porter un casque homologué et des vêtements adaptés à la pratique du cyclisme (notamment visible en soirée et la nuit) ;</li>
<li>Imposer le port du casque homologué aux enfants de moins de douze (12) ans ;</li>
<li>Adapter sa distance de freinage notamment en cas d’intempéries ;</li>
<li>Effectuer le réglage de la selle pour adapter sa hauteur à sa morphologie ;</li>
<li>Monter et utiliser, seul, le vélo ; le transport de personne sur le vélo par tout moyen (p. ex., sur le porte-bagage) est strictement interdit, à l’exclusion du transport d’un enfant sur un siège-bébé ou une remorque ;</li>
<li>Ne pas utiliser le vélo et les équipements à des fins commerciales (ex. : service de livraison, coursier, etc.) ;</li>
<li>Utiliser les équipements et spécialement les sacoches et les porte-bagages conformément à leur usage. Il est rappelé qu’un chargement sur un porte-bagage ne doit pas excéder 25kg.</li>
</ul>
<h4>8.4 : Transport du / des vélo(s)</h4>
Le client transporte le(s) vélo(s) et les équipement(s) sous sa seule responsabilité.
Le transport du / des vélo(s) sur un véhicule (voiture, camion, etc.) implique de disposer du matériel adéquat (galerie ou porte-vélo) et d’en assurer sa protection.
Dans le cas où le client souhaiterait transporter le(s) vélo(s) et les équipement(s) via le train, il doit s’assurer que :
<ul>
<li>Le(s) vélo(s) et les équipement(s) puissent être effectivement embarqués dans le train et effectuer les réservations auprès des compagnies ferroviaires concernés ;</li>
<li>Prendre à sa charge les coûts de transport.</li>
</ul>
A défaut, le client sera seul responsable des conséquences et retards éventuels de restitution des vélo(s) et équipement(s). Le cas échéant, L[P]AV se réserve le droit de facturer au client tout décalage et journée de location supplémentaires.
<h4>8.5 : Vol ou sinistre</h4>
Lors de chaque période d’inutilisation du / des vélo(s), le client s’engage à systématiquement attacher le cadre du vélo et la roue arrière à un support fixe du mobilier urbain à l’aide de l’antivol fourni par L[P]AV.
<br>En cas de vol, le client doit déposer plainte auprès des services de police. Il s’engage, en outre, à contacter L[P]AV sans délai via le numéro suivant :
<b>06 71 08 12 72</b>
<br>En tout état de cause, L[P]AV facturera au client le prix du vélo mentionné dans le devis.
<h4>8.6 : Assurance responsabilité civile individuelle</h4>
Le client s’engage à souscrire à et / ou déclare être titulaire d’une assurance personnelle en Responsabilité Civile qui garantit les conséquences de l’utilisation du / des vélo(s) pour lui et pour les personnes dépendant de lui (enfants mineurs).
<h4>8.7 : Aptitude physique</h4>
Le client reconnaît être apte à la pratique du cyclisme et n’avoir aucune contre-indication médicale.
 
